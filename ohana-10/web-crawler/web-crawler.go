package main

import (
	"fmt"
	"sync"
)

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

type CheckURL struct {
	check map[string]bool
	m     sync.Mutex
}

var url_check = CheckURL{check: make(map[string]bool)}

func (c *CheckURL) duplicated(url string) bool {
	c.m.Lock()
	defer c.m.Unlock()
	_, benar := c.check[url]
	if benar {
		return true
	} else {
		c.check[url] = true
	}
	return false
}

func Crawler(url string, depth int, fetcher Fetcher) {
	var wg sync.WaitGroup
	wg.Add(1)
	go Crawl(url, depth, fetcher, &wg)
	wg.Wait()
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func Crawl(url string, depth int, fetcher Fetcher, wg *sync.WaitGroup) {
	// TODO: Fetch URLs in parallel.
	// TODO: Don't fetch the same URL twice.
	// This implementation doesn't do either:
	defer wg.Done()

	if depth <= 0 || url_check.duplicated(url) {
		return
	}

	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}
	fmt.Printf("found: %s %q\n", url, body)
	for _, u := range urls {
		wg.Add(1)
		go Crawl(u, depth-1, fetcher, wg)
	}
	return
}

func main() {
	Crawler("https://golang.org/", 1, fetcher)
}

// fakeFetcher is Fetcher that returns canned results.
